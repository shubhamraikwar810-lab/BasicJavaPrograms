package Day3;

public class TernaryOperator 
{

	public static void main(String[] args) 
	
	{
		// var = exp ? result 1:result2
		
		int a=200, b=100;
		int res = (a>b)?a:b;  //(a>b) = expression, res = varaiable, 
		// Now if expression is true than first result (a) will be assigned to res, if expression is false then second value (b) will be assigned to res
		
	System.out.println(res);
	
	//ex2
	
	
		

	}

}
